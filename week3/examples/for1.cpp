// Program For1
// Example using a for-statements, increment

// for-statement consists of 3 parts...
// 1-The Loop Variable
// 2-The Terminating Condition
// 3-The Expression that Updates the Loop Variable

#include <iostream>

using namespace std;

int main()
{
    for (int x = 0; x < 10; x++)
    {
        cout << x << ' ';
    }

    return 0;
}
